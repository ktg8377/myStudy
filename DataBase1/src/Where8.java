//	@is null 연산자
//	null을 위한 연산자
//	오라클에서는 컬럼에 null값이 저장되는 것을 허용한다
//	그러나 null은 매우 특이한 성격을 가진 데이터이므로
//	null값을 제대로 이해하지 못하면 원하는 결과를 얻지 못할 수 있다
//	
//	NULL은 값이 존재하지 않는 것으로 
//	확정되지 않은 값을 표현할 때 사용한다
//	따라서 어떤 값보다 크거나 작지도 않고 "공백"이나 "0"과 달리 비교 자체가
//	불가능한 값이다
//	
//	NULL의 특성
//	-NULL 값과의 수치연산은 NULL 값을 리턴한다
//	-NULL 값과의 비교연산은 거짓(FALSE)을 리턴한다
//	-어떤 값과 비교할 수도 없으며, 특정 값보다 크다, 적다라고
//	표현할 수 없다
//	
//	NULL 값과 비교 연산은 오직 IS NULL, IS NOT NULL이라는
//	정해진 문구를 사용해야 결과를 얻을 수 있다
	
