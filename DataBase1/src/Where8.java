//	
//	AND		AND라고 적는다		두 결과가 참이면 TRUE
//	OR		OR라고 적는다		하나가 참이면 TRUE 둘 다 FALSE이면 FALSE
//	NOT		부정연산자이다 NOT이라고 적는다 조건의 결과를 되돌려 준다
	
